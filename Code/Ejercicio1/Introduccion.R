Muchas comparaciones, como las antes mencionadas, se hacen con base en el diseño
completamente aleatorizado (DCA), que es el más simple de todos los diseños que se utilizan
para comparar dos o más tratamientos, dado que sólo consideran dos fuentes de
variabilidad: los tratamientos y el error aleatorio.

En caso de que los tratamientos tengan efecto, las observaciones \\(Y_{ij}\\) se podrán 
describir con el modelo estadístico lineal dado por:
$$Y_{ij} = \\mu+ \\tau_i + e_{ij} $$
donde \\(\\mu \\) es el parámetro de escala común a todos los tratamientos, llamado media
global, \\(\\tau_i\\) ; es un parámetro que mide el efecto del tratamiento \\(i\\) y \\(e_{ij}\\) 
es el error atribuible a la medición \\(Y_{ij}\\) . Este modelo implica que en el diseño completamente al azar
actuarán a lo más dos fuentes de variabilidad: los tratamientos y el error aleatorio.

